            <h3><u><strong>LE SECTEUR D’ACTIVITE : TRANSPORT MARITIME</strong></u></h3>
<br>
            <p>Avec 9 milliards de tonnes de marchandises transportées par an, la voie maritime est le premier mode 
            de transport de marchandises et occupe une place grandissante dans le commerce international. 
            C’est un mode peu onéreux mais dont les délais d’acheminements sont plus longs. Les marchandises peuvent être acheminées sur des navires spécialisés polythermes 
            destinés au transport des denrées périssables ou sur des navires non spécialisés (cargos conventionnels, porte-conteneurs, navires mixtes, porte-barges pour transport combiné fluvial/maritime). 
<br><br>
            La conteneurisation permet les coûts les plus bas et offre un certain nombre d’avantages tels que la sécurité de la chaîne du froid, 
            la traçabilité, la rapidité, les services et livraisons en porte à porte. Les progrès technologiques liés aux conditionnements, 
            à la mécanisation, à la réfrigération des navires (produits sous atmosphère contrôlée et sous atmosphère modifiée), 
            le suivi des marchandises via des systèmes informatisés ont permis au transport maritime de devenir compétitif pour l’acheminement des produits frais, 
            entrainant une baisse des coûts.
<br><br>            

            Le fret frigorifique transporté à travers le monde concerne en grande partie la viande, les poissons, 
            les produits laitiers, les fruits et légumes (dont 80% de bananes, agrumes et autres...). Pour les acheminer, on fait appel aux navires « reefer » (un navire frigorifique), 
            le mode le plus économique et le moins polluant. Cependant, il est concurrencé par la conteneurisation qui profite de la croissance des échanges de produits frais et 
            de la préférence de clients (dont la grande distribution) sensibles aux tarifs et aux approvisionnements en flux tendus.
<br><br>            

            Néanmoins, le manque de flexibilité des infrastructures portuaires (ports de chargement/déchargement) et son coût élevé, sont autant de freins à l’essor de cette économie.
            Toutefois, le contexte économique favorable (globalisation des marchés de consommation, 
            libéralisation des échanges) pérennise la place du transport maritime dans les échanges commerciaux.
<br><br>            
